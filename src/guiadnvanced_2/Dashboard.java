    private void cargarDatosDesdeArchivo() throws FileNotFoundException, IOException {
        //this.manejadorArchivo.
        this.apuntadorLectura = new FileReader (apuntadorArchivo);
        this.manejadorLectura = new BufferedReader(apuntadorLectura);

         // Lectura del fichero
        String linea;
        while((linea=this.manejadorLectura.readLine())!=null){
            //Acá hace un split del archivo
            //-----------------------------
            String[] partes = linea.split(",");
            //Verifica si el archivo tiene al menos un dato
            //System.out.println(partes.length);
            if(partes.length>0){
                //--------
                //Valida que el detalle estè vacìo, si lo està (es null) entonces
                //guarda una cadena vacìa
                if(partes[4].equals("null")){
                   partes[4]="";
                }
                Producto Productoagregar= new Producto(
                    partes[0],
                    Integer.parseInt(partes[1]),
                    Double.parseDouble(partes[2]),
                    partes[3],
                    partes[4]
                );
                //-----
                this.listadoProductos.add(Productoagregar);
                forma1AdicionDatos(this.listadoProductos);
            }
        }
    }

    private void escribirDatosEnArchivo() throws IOException {
        //Me pregunto si el arcivo existe
            if(this.apuntadorArchivo.exists()) {
                this.manejadorArchivo = new BufferedWriter(new FileWriter(this.apuntadorArchivo,false));
            } else {
                this.manejadorArchivo = new BufferedWriter(new FileWriter(this.apuntadorArchivo,false));
            }
            for (int i = 0; i < this.listadoProductos.size(); i++) {   
                Producto itemAgregar=(Producto)this.listadoProductos.get(i);
                this.manejadorArchivo.write(
                    itemAgregar.getNombre()+","+
                    String.valueOf(itemAgregar.getCantidad())+","+
                    String.valueOf(itemAgregar.getCosto())+","+
                    itemAgregar.getUnidad()+","+
                    itemAgregar.getDetalle()
                );
                this.manejadorArchivo.newLine();
            //this.manejadorArchivo.write("\n");             
            }
            this.manejadorArchivo.close();
            //-------
    }

    private int buscarYSeleccionarUnidadMedicion(String unidadSeleccionada) {
        //Debe recorrer la lista de unidades de medición
        for (int i = 0; i < this.UnidadesMedicion.size(); i++) {
            //---------------
            UnidadMedicion itemUnidad=(UnidadMedicion)this.UnidadesMedicion.get(i);
            if(unidadSeleccionada.equals(itemUnidad.getDetalle()+" ("+itemUnidad.getNombre()+")")){
              return i;  
            }
        }
        return -1;
    }

