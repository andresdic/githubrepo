    private boolean esnumero(String text) {
        try{
            Integer.parseInt(text);
            return true;
        } catch (Exception e){
            return false;
        }
    }

    private boolean esdouble(String text) {
       try{
           Double.parseDouble(text);
           return true;
       }catch(Exception e){
           return false;
       }
    }

    private boolean esletra(String text) {
       if(text.length()>=3)
           return true;
       else{
           return false;
       }
    }
