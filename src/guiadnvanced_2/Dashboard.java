    private void botonBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonBorrarMouseClicked
        // TODO add your handling code here:
        //Capturamos la fila seleccionada
        int filaSeleccionada=this.tablaProductos.getSelectedRow();
        if(filaSeleccionada>=0){
            //Borrra la fila de la tabla
            ((DefaultTableModel)this.tablaProductos.getModel()).removeRow(filaSeleccionada);
            //Borra fila de la lista
            this.listadoProductos.remove(filaSeleccionada);
        }
        else{
            this.panelMensajeLista.setBackground(Color.red);
            this.cuadroMensajeLista.setText("Debe seleccionar una fila para efectuar el borrado");
        }
    }//GEN-LAST:event_botonBorrarMouseClicked

